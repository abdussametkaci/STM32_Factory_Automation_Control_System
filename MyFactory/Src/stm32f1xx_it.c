/**
* @brief This function handles EXTI line[9:5] interrupts.
*/
void EXTI9_5_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI9_5_IRQn 0 */
	if(HAL_GPIO_ReadPin(GPIOA, addItem_Pin)){
		if(isActive && total <= 199){
			if(convertADC(temperature, 12, 6) <= 32){
				grupA++;
				HAL_GPIO_WritePin(grupA_led_GPIO_Port, grupA_led_Pin, GPIO_PIN_SET);
				HAL_GPIO_WritePin(grupB_led_GPIO_Port, grupB_led_Pin, GPIO_PIN_RESET);
			} else {
				grupB++;
				HAL_GPIO_WritePin(grupA_led_GPIO_Port, grupA_led_Pin, GPIO_PIN_RESET);
				HAL_GPIO_WritePin(grupB_led_GPIO_Port, grupB_led_Pin, GPIO_PIN_SET);
			}
			
			additionTime = 0;
		}
	}
	
	total = grupA + grupB;
	
  /* USER CODE END EXTI9_5_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_8);
  /* USER CODE BEGIN EXTI9_5_IRQn 1 */

  /* USER CODE END EXTI9_5_IRQn 1 */
}

/**
* @brief This function handles TIM3 global interrupt.
*/
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0 */
	additionTime++;
	
	if(!isActive){
		additionTime = 0;
	} else {
		if(total > 199){
			HAL_UART_Transmit_IT(&huart1, error1, 30);
		} else if(additionTime > 20){ 			// 10 second
			HAL_UART_Transmit_IT(&huart1, error2, 30);
		}
	
		temperature = HAL_ADC_GetValue(&hadc1);
		HAL_ADC_Start(&hadc1);
	}
	
	
  /* USER CODE END TIM3_IRQn 0 */
  HAL_TIM_IRQHandler(&htim3);
  /* USER CODE BEGIN TIM3_IRQn 1 */

  /* USER CODE END TIM3_IRQn 1 */
}

/**
* @brief This function handles TIM4 global interrupt.
*/
void TIM4_IRQHandler(void)
{
  /* USER CODE BEGIN TIM4_IRQn 0 */
	if(isActive){
		formatString(message, grupA, grupB);
		HAL_UART_Transmit_IT(&huart1, message, length(message));
	}
	
  /* USER CODE END TIM4_IRQn 0 */
  HAL_TIM_IRQHandler(&htim4);
  /* USER CODE BEGIN TIM4_IRQn 1 */

  /* USER CODE END TIM4_IRQn 1 */
}

